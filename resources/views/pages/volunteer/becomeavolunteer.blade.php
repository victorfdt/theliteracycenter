<h3>Thinking about volunteering?</h3>
<p>
	Most volunteers want to tutor a student one-to-one. Below you’ll find information about becoming
	a tutor and other volunteer opportunities we hope you’ll consider. When you are ready to volunteer,
	please <a target="_blank" href="http://www.litcenter.org/volunteer-form/" title="Volunteer Information Form">submit this form.</a>
</p>

<br>

<h3>Tutor</h3>
<p>
	Provide one-on-one basic reading and writing instructions to an adult eighteen years of age or older.
	Help student acquire basic skills and use them to meet self-identified goals. Approximately 8 hours
	of orientation and training is required. A minimum 1-year term of service is required. During the year,
	tutors are required to complete one in-service training, which usually requires 2-6 hours.
</p>
<p>
	Qualifications for tutors include good reading/writing skills, a desire to teach an adult, patience and
	perseverance to assist students in reaching personal reading goals, empathy and enthusiasm, and reliability
	to keep records and report to The Literacy Center in a timely manner.
</p>

<br>

<h3>Evaluator</h3>
<p>
	An evaluator interviews students, administers skills assessment tests, and prepares reports of results.
	Approximate time is 2 hours per student, on an as-needed basis, and according to your availability. Tutor
	training and evaluator training are required, and participation as a tutor is recommended.
</p>

<br>

<h3>Trainer</h3>
<p>
	Experienced tutors or teachers make valuable trainers. Volunteers who would like to become trainers
	should let the TLC Director know.
</p>

<br>

<h3>Board Member</h3>
<p>
	To volunteer in this area, you must have the interest and time to devote to The Literacy Center
	as a consultant in a particular area of non-profit operation. In addition, you must be able to
	attend monthly board meetings, and participation in appropriate sub-committee meetings is expected.
</p>
<br>

<h3>Committees / Event Help</h3>
<p>
	To volunteer on a committee we ask that you assist us with organizing and making our events
	successful. The time committment is likely to be an hour or two a week for a period of two to three months.
</p>

