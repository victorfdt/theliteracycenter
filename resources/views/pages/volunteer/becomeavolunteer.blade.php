<h3>SPEAKER</h3>
<p>
	Volunteers on our Speaker’s Bureau give small presentations to local community groups and/or businesses. 
	The volunteer must meet with the Director for preparation. We provide materials for the speaker, and will contact
	you as request for speakers are received. Participation in other volunteer areas is recommended.
</p>

<br>

<h3>OFFICE SUPPORT</h3>
<p>
	This volunteer would have a regular weekly schedule (minimum 2 hours per week) at The Literacy Center. 
	Duties would include answering phones, making appointments for students, and assisting with special projects, 
	such as making flashcards or photocopying. An outgoing, helpful personality is best for this position.
</p>

<br>

<h3>LITERACY LAB</h3>
<p>
	This volunteer could have a regular weekly schedule (minimum 1 hour per week) at The Literacy Center.  
	Duties would include assisting students in the lab with using the computers for reading instruction. 
	The software is <em><strong>Lexia</strong></em> software and is easy for a volunteer to learn quickly. A friendly, 
	patient individual is best for this position.
</p>
