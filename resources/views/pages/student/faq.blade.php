<h3>What levels of reading do you teach?</h3>
<p>The National Adult Literacy Survey of 1993 defined literacy as: “Using printed and written information to function in society, to achieve one’s goals, and to develop one’s knowledge and potential.” This definition does not tie literacy to one standard ability or grade level, but recognizes that literacy is related to the needs of each individual.</p>
<p>For this reason, the Literacy Center does not simply teach general reading and writing courses. Instead, we strive to help our clients achieve functional literacy, meaning they will acquire the reading and writing skills they need to meet their own goals. For example, some clients will want help studying for their high school equivalency exam/TASC (previously known as the GED) while others may want to be able to pass a driver’s license exam or be able to read a bedtime story to their children.</p>
<p>In general, we help adults who are reading below an eighth grade level, and most of our students come to us reading below 5th grade level.</p>

<br>

