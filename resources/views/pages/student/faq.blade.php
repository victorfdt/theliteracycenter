<h3>Where do you get your students?</h3>
<p>Most of our students are referred to us by someone to whom they have confided. This includes a counselor at social service agency, church member, friend, or relative. Most of our students have wanted help for a while, but didn’t know where to turn.</p>

<br>

<h3>Why are there so many adults who can’t read?</h3>
<p>Most researcher’s estimates show that about 20% of the population have learning disabilities. Much more is being learned through recent research about learning disabilities, and we know that the most common one affecting our students is dyslexia.</p>
<p>A person with a learning disability needs to be taught in a specific manner, using techniques that take more time (and in some cases more specialty training) than many schools can offer to every child. Schools simply don’t have enough financial resources to provide every child with the specialized training needed to help them overcome their learning disability.</p>
<p>Although some of our adult students have cognitive disabilities or psychological disorders that have made learning difficult, it’s important to note that most of the adults we see have average or above average intelligence. They just have a learning disability.</p>

<br>

<h3>Do I need teaching experience to be a reading tutor?</h3>
<p>No! Tutors need not have previous tutoring or teaching experience. The Literacy Center provides training, and our office staff and volunteers are available to answer questions and address concerns.</p>

<br>

<h3>How much time will be required to be a tutor?</h3>
<p>Our tutors are required to make a one year commitment-that is the amount of time our students must commit to, and we want them to work with the same tutor for the entire year. As a tutor, you will meet with a student twice week for approximately 1-1.5 hours each session. You will plan and guide the lessons. With preparation time and travel time included, you can expect to be giving 4-5 hours a week of your time.</p>

